/**
* @brief Function implementing the SensorTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartSensorTask */
void StartSensorTask(void const * argument)
{
  /* USER CODE BEGIN StartSensorTask */
	uint8_t temperature = 0;
	uint8_t humidity = 0;
	uint8_t read_success = 0;
	SensorData_t sensorData;

	printf("DHT11 Sensor Task Started!\r\n");

  /* Infinite loop */
  for(;;)
  {
	// Try to read DHT11
	read_success = DHT11_Read(&temperature, &humidity);

    if (read_success) {
    	// Success - send data to queue
    	sensorData.temperature = temperature;
    	sensorData.humidity = humidity;

    	osMessagePut(SensorDataQueueHandle, *(uint32_t*)&sensorData, 0);

    	// Visual indicator
    	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET); // Orange ON = good read

    	printf("Temp: %d C, Humidity: %d%%\r\n", temperature, humidity);

    	// Blink red LED if hot
    	if (temperature > 25) {
    		HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14); // Red LED
    	} else {
    		// Cool - turn red LED off
    	    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
    	}
    } else {
    	// Failed to read
    	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13); // Orange blink = error
    	printf("DHT11 read failed\r\n");
    }

    osDelay(2000); // Read every 2 seconds (DHT11 needs time between reads)
  }
  /* USER CODE END StartSensorTask */
}

/* USER CODE BEGIN Header_StartDisplayTask */
/**
* @brief Function implementing the DisplayTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartDisplayTask */
void StartDisplayTask(void const * argument)
{
  /* USER CODE BEGIN StartDisplayTask */
  osEvent evt;
  SensorData_t receivedData;
  char tempStr[32];

  // Initialize display
  osMutexWait(DisplayMutexHandle, osWaitForever);
  SSD1306_Init();
  SSD1306_Clear();
  SSD1306_GotoXY(0, 0);
  SSD1306_WriteString("Waiting for");
  SSD1306_GotoXY(0, 2);
  SSD1306_WriteString("sensor data...");
  SSD1306_UpdateScreen();
  osMutexRelease(DisplayMutexHandle);

  printf("Display Task Started!\r\n");

  /* Infinite loop */
  for(;;)
  {
	// Wait for sensor data from queue
	evt = osMessageGet(SensorDataQueueHandle, 3000); // 3 second timeout

    if (evt.status == osEventMessage) {
      // Got data from queue
      receivedData = *(SensorData_t*)&evt.value.v;

      // Lock the display mutex
      osMutexWait(DisplayMutexHandle, osWaitForever);

      // Update display
      SSD1306_Clear();

      // Line 1: Title
      SSD1306_GotoXY(0, 0);
      SSD1306_WriteString("ENV MONITOR");

      // Line 3: Temperature
      SSD1306_GotoXY(0, 2);
      sprintf(tempStr, "Temp: %d C", receivedData.temperature);
      SSD1306_WriteString(tempStr);

      // Line 5: Humidity
      SSD1306_GotoXY(0, 4);
      sprintf(tempStr, "Humid: %d%%", receivedData.humidity);
      SSD1306_WriteString(tempStr);

      // Line 7: Status
      SSD1306_GotoXY(0, 6);
      if (receivedData.temperature > 25) {
        SSD1306_WriteString("Status: HOT!");
      } else {
        SSD1306_WriteString("Status: OK");
      }

      SSD1306_UpdateScreen();

      // Release mutex
      osMutexRelease(DisplayMutexHandle);

      printf("Display updated\r\n");
    } else {
      // Timeout - no data received
      osMutexWait(DisplayMutexHandle, osWaitForever);
      SSD1306_Clear();
      SSD1306_GotoXY(0, 2);
      SSD1306_WriteString("No sensor data");
      SSD1306_UpdateScreen();
      osMutexRelease(DisplayMutexHandle);
    }

	osDelay(100); // Small delay
  }
  /* USER CODE END StartDisplayTask */
}

