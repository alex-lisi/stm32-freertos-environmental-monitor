// Read DHT11 sensor - returns 1 on success, 0 on failure
uint8_t DHT11_Read(uint8_t *temperature, uint8_t *humidity) {
    uint8_t data[5] = {0};
    uint32_t timeout;

    // Send start signal
    DHT11_SetPinOutput();
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_0, GPIO_PIN_RESET);
    HAL_Delay(18); // Hold low for at least 18ms
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_0, GPIO_PIN_SET);
    delay_us(30);

    // Switch to input mode
    DHT11_SetPinInput();

    // Wait for DHT11 response (should go low)
    timeout = 1000;
    while (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_0) == GPIO_PIN_SET && timeout--);
    if (timeout == 0) return 0;

    // Wait for DHT11 to go high
    timeout = 1000;
    while (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_0) == GPIO_PIN_RESET && timeout--);
    if (timeout == 0) return 0;

    // Wait for DHT11 to go low (start of data)
    timeout = 1000;
    while (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_0) == GPIO_PIN_SET && timeout--);
    if (timeout == 0) return 0;

    // Read 40 bits of data
    for (int i = 0; i < 40; i++) {
        // Wait for pin to go high
        timeout = 1000;
        while (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_0) == GPIO_PIN_RESET && timeout--);
        if (timeout == 0) return 0;

        delay_us(40);

        // If still high after 40us, it's a '1'
        if (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_0) == GPIO_PIN_SET) {
            data[i / 8] |= (1 << (7 - (i % 8)));
        }

        // Wait for pin to go low
        timeout = 1000;
        while (HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_0) == GPIO_PIN_SET && timeout--);
        if (timeout == 0) return 0;
    }

    // Verify checksum
    if (data[4] != ((data[0] + data[1] + data[2] + data[3]) & 0xFF)) {
        return 0; // Checksum failed
    }

    // Extract values
    *humidity = data[0];
    *temperature = data[2];

    return 1; // Success
}

